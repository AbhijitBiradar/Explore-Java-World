package com.automation.general;

/**
* Java Program to Find ASCII value of a character
*
* @author  Abhijit
* @version 1.0
* @since   2019-05-19
*/

public class FindASCII {

	public static void main(String[] args) {
		char ch='a';
				
		int castAscii=(int)ch;
		
		System.out.println("Given character value:"+ ch);
		
		System.out.println("ASCII value of above character: "+ castAscii);	
	}
}
