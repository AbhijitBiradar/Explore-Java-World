package com.automation.general;

/**
* Java Program to Display Characters from A to Z using loop
*
* @author  Abhijit Biradar
* @version 1.0
* @since   2019-05-20
*/

public class DiplayCharacter {

	public static void main(String[] args) {
		
		char ch;
		for(ch='A';ch<='Z';ch++) {
			System.out.print(" "+ ch);
		}
	}
}
